import { createClient } from "@supabase/supabase-js" // Base de dados no Supabase, com bucket
import multer from "multer"
import dotenv from "dotenv"
//////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////

dotenv.config()
//////////////////////////////////////////////////////////////////////////////////////////////////

const supabase = createClient(
    process.env['SUPABASE_URL'],
    process.env['SUPABASE_KEY']
)
//////////////////////////////////////////////////////////////////////////////////////////////////

const BUCKET_NAME = process.env['SUPABASE_BUCKET']
//////////////////////////////////////////////////////////////////////////////////////////////////

// Multer guarda o arquivo em memória temporariamente
const uploadFile = multer({ storage: multer.memoryStorage() })
//////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////

// Salvamento de imagem do produto
async function saveFile(file) {
    const fileName = Date.now() + '-' + file.originalname
    const { error } = await supabase.storage
        .from(BUCKET_NAME)
        .upload(fileName, file.buffer, { contentType: file.mimetype })

    if (error) throw error
    return fileName
}
//////////////////////////////////////////////////////////////////////////////////////////////////

// Obtenção do URL da imagem do produto
function getFileUrl(fileName) {
    const { data } = supabase.storage
        .from(BUCKET_NAME)
        .getPublicUrl(fileName)
    return data.publicUrl
}
//////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////

export default { uploadFile, saveFile, getFileUrl }